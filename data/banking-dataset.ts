export const datasetSchema = `
Dataset: Banking Analytics (${bankingDataset.length} records)
Fields:
- product_apply: Product type applied for (KTA=Personal Loan, KPR=Mortgage, CC=Credit Card)
- app_id: Application identifier
- segment: Customer segment (ETB=Existing-to-Bank, NTB=New-to-Bank, Payroll=Payroll customer)
- collectibility: Loan collectibility status (1=Current, 2=Special Mention, 3=Substandard, 4=Doubtful, 5=Loss)
- AUM_IDR: Assets Under Management in IDR (Indonesian Rupiah)
- MOB_months: Months on Books (loan tenure in months)
- PL_balance: Personal Loan outstanding balance (IDR)
- payroll_credit_M1/M2/M3: Payroll credits for last 3 months (IDR)
- CC_score: Credit card score
- CC_block_code: Credit card block status (N=Normal, B=Blocked, C=Cancelled, D=Delinquent)
- CC_limit: Credit card limit (IDR)
- CC_balance: Credit card outstanding balance (IDR)
`;

export function getDatasetSummary(): string {
  const segmentCounts = bankingDataset.reduce<Record<string, number>>(
    (acc, r) => {
      acc[r.segment] = (acc[r.segment] ?? 0) + 1;
      return acc;
    },
    {}
  );

  const productCounts = bankingDataset.reduce<Record<string, number>>(
    (acc, r) => {
      acc[r.product_apply] = (acc[r.product_apply] ?? 0) + 1;
      return acc;
    },
    {}
  );

  const avgAUM =
    bankingDataset.reduce((sum, r) => sum + r.AUM_IDR, 0) /
    bankingDataset.length;

  const avgCCUtilization =
    bankingDataset.reduce(
      (sum, r) => sum + (r.CC_limit > 0 ? r.CC_balance / r.CC_limit : 0),
      0
    ) / bankingDataset.length;

  return `Summary: ${bankingDataset.length} customers, Segments: ${JSON.stringify(segmentCounts)}, Products: ${JSON.stringify(productCounts)}, Avg AUM: ${Math.round(avgAUM).toLocaleString()} IDR, Avg CC Utilization: ${(avgCCUtilization * 100).toFixed(1)}%`;
}
